def _session_fp(n, ts1):
    """Fingerprint of a session's state — changes only when the session changes."""
    return f"{n}:{ts1}"


def _recap_prompts(path):
    """Collect the user prompts from a session log, for feeding to the recap model."""
    out = []
    with open(path, errors="ignore") as fh:
        for line in fh:
            try:
                r = json.loads(line)
            except Exception:
                continue
            if r.get("type") == "user" and not r.get("isMeta"):
                t = _msg_text(r.get("message", {})).strip().replace("\n", " ")
                if t and not t.startswith("<") and not t.startswith("Caveat"):
                    out.append(t[:300])
    return out


def _gen_recap(project, prompts, files, summary):
    """One-sentence recap via `claude -p` (same login the sessions use). None on failure."""
    sel = prompts[:4] + (prompts[-3:] if len(prompts) > 4 else [])
    parts = [
        "Write a single-sentence recap (max 22 words) of this coding session for a "
        "developer's dashboard. Say what was worked on and where it left off. Be specific "
        "and concrete. Output ONLY the sentence — no preamble, no quotes.\n",
        f"Project: {project}",
    ]
    if summary:
        parts.append(f"Auto-summary: {summary}")
    if files:
        parts.append("Files touched: " + ", ".join(os.path.basename(f) for f in files[:8]))
    parts.append("User prompts (chronological):\n" + "\n".join(f"- {p}" for p in sel))
    prompt = "\n".join(parts)
    try:
        res = subprocess.run(
            ["claude", "-p", "--model", RECAP_MODEL, prompt],
            capture_output=True, text=True, timeout=90,
        )
        txt = (res.stdout or "").strip().strip('"')
        return txt or None
    except Exception:
        return None


def _recap_refresh():
    """Generate recaps for sessions that are new or have changed since last time."""
    files = sorted(glob.glob(os.path.join(SESS_DIR, "*.jsonl")),
                   key=os.path.getmtime, reverse=True)[:500]
    cache = _recap_load()
    todo = []
    for f in files:
        s = _scan_session(f)
        pj = _proj_of(s["cwd"])
        if not pj or not s["ts0"] or s["n"] == 0:
            continue
        sid = os.path.basename(f)[:-6]
        fp = _session_fp(s["n"], s["ts1"])
        if cache.get(sid, {}).get("fp") != fp:
            todo.append((sid, f, s, pj[0], fp))
    if not todo:
        return 0

    def work(item):
        sid, f, s, project, fp = item
        recap = _gen_recap(project, _recap_prompts(f), list(s["edited"].keys()), s.get("summary"))
        return sid, recap, fp

    done = 0
    from concurrent.futures import ThreadPoolExecutor
    with ThreadPoolExecutor(max_workers=RECAP_CONCURRENCY) as ex:
        for sid, recap, fp in ex.map(work, todo):
            if recap:
                cache[sid] = {"recap": recap, "fp": fp,
                              "at": datetime.datetime.now(datetime.timezone.utc).isoformat()}
                _recap_save(cache)  # incremental, so the menu fills in progressively
                done += 1
    return done


