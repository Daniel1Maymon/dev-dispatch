def _parse_card_json(text):
    """Pull a JSON object out of the model's reply, tolerating stray prose/fences."""
    t = (text or "").strip()
    if t.startswith("```"):
        t = re.sub(r"^```[a-zA-Z]*\n?|\n?```$", "", t).strip()
    i, j = t.find("{"), t.rfind("}")
    if i == -1 or j == -1:
        return None
    try:
        return json.loads(t[i:j + 1])
    except Exception:
        return None


def _gen_issuecard(project, label, prompts, summary, recap):
    """Structured card (issue / status / next / blocked) for ONE session via `claude -p`."""
    sel = prompts[:3] + (prompts[-3:] if len(prompts) > 3 else [])
    prompt = (
        "You summarize ONE coding session as a single 'issue' on a developer's ADHD "
        "re-focus board. The developer forgets state when focus drops, so be concrete and "
        "name the real thing. Output ONLY a JSON object (no markdown, no prose) with EXACTLY:\n"
        '  "issue": a short label for what this session is about (<=7 words)\n'
        '  "status": one of "in_progress" | "blocked" | "winding_down" | "done"\n'
        '  "next": ONE concrete next action for this session (<=12 words, imperative)\n'
        '  "blocked": a short open problem/blocker, or "" if none\n\n'
        f"Project: {project}" + (f" / {label}" if label and label != project else "") + "\n"
        + (f"Auto-summary: {summary}\n" if summary else "")
        + (f"Earlier recap: {recap}\n" if recap else "")
        + "User prompts (chronological):\n" + "\n".join(f"- {p}" for p in sel)
    )
    try:
        res = subprocess.run(
            ["claude", "-p", "--model", RECAP_MODEL, prompt],
            capture_output=True, text=True, timeout=120,
        )
        card = _parse_card_json(res.stdout)
        if not card or "next" not in card:
            return None
        return {
            "issue": card.get("issue") or "",
            "status": card.get("status") or "in_progress",
            "next": card.get("next") or "",
            "blocked": card.get("blocked") or "",
        }
    except Exception:
        return None


def _focus_sessions():
    """The (sessionId -> session dict) set we keep issue-cards warm for: the issues inside
    the top FOCUS_POOL subjects, capped to FOCUS_ISSUES sessions each."""
    ctx = _build_context()
    wanted = {}
    for t in (ctx.get("topics") or [])[:FOCUS_POOL]:
        for s in t["sessions"][:FOCUS_ISSUES]:
            wanted[s["id"]] = s
    return wanted


def _issuecard_refresh():
    """Generate/refresh issue cards for the sessions visible on the focus board."""
    wanted = _focus_sessions()
    cache = _issuecard_load()
    todo = [s for sid, s in wanted.items()
            if cache.get(sid, {}).get("fp") != _session_fp(s["msgs"], s["end"])]
    if not todo:
        return 0

    def work(s):
        path = os.path.join(SESS_DIR, s["id"] + ".jsonl")
        prompts = _recap_prompts(path) if os.path.exists(path) else [s.get("prompt", "")]
        card = _gen_issuecard(s.get("project", ""), s.get("label", ""),
                              prompts, s.get("summary"), s.get("recap"))
        return s["id"], card, _session_fp(s["msgs"], s["end"])

    done = 0
    from concurrent.futures import ThreadPoolExecutor
    with ThreadPoolExecutor(max_workers=RECAP_CONCURRENCY) as ex:
        for sid, card, fp in ex.map(work, todo):
            if card:
                cache[sid] = {"card": card, "fp": fp,
                              "at": datetime.datetime.now(datetime.timezone.utc).isoformat()}
                _issuecard_save(cache)
                done += 1
    return done


_SUBJECT_RANK = {"blocked": 0, "in_progress": 1, "winding_down": 2, "done": 3}


def _subject_status(issues):
    """Roll up a subject's status from its issues: blocked > in_progress > winding_down > done."""
    statuses = [i["card"].get("status") or "in_progress" for i in issues if i.get("card")]
    if not statuses:
        return "in_progress"
    return min(statuses, key=lambda s: _SUBJECT_RANK.get(s, 1))


