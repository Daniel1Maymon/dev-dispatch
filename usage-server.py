def _subject_fp(topic):
    """Fingerprint of a subject — changes when ANY of its sessions gains activity, so an
    archived subject auto-revives the moment you touch it again."""
    parts = sorted(f"{s['id']}:{s.get('msgs')}:{s.get('end')}" for s in topic["sessions"])
    return str(hash("|".join(parts)))


def set_archived(subject_key, undo, topics):
    """Persist an archive/undo action. Returns the new archived state for that key."""
    arch = _archive_load()
    if undo:
        arch.pop(subject_key, None)
    else:
        t = next((x for x in topics if x["id"] == subject_key), None)
        arch[subject_key] = {
            "archivedAt": datetime.datetime.now(datetime.timezone.utc).isoformat(),
            "fp": _subject_fp(t) if t else None,
        }
    _archive_save(arch)
    return not undo


def _eligible_subjects(topics):
    """Filter out stale and archived subjects (auto-reviving any that got new activity)."""
    arch = _archive_load()
    stale_cut = (datetime.datetime.now(datetime.timezone.utc)
                 - datetime.timedelta(days=STALE_DAYS)).isoformat()
    eligible, archived_now, changed = [], [], False
    for t in topics:
        if t["lastActivity"] < stale_cut:
            continue  # staleness gate (objective; nothing saved)
        a = arch.get(t["id"])
        if a:
            if a.get("fp") == _subject_fp(t):
                archived_now.append({"id": t["id"], "title": t["title"],
                                     "lastActivity": t["lastActivity"]})
                continue  # still archived — you haven't touched it since
            arch.pop(t["id"], None)  # new activity -> auto-revive
            changed = True
        eligible.append(t)
    if changed:
        _archive_save(arch)
    return eligible, archived_now


def _build_focus():
    """The refocus board: top subjects (topics), each with its issues (sessions) as cards."""
    ctx = _build_context()
    topics = ctx.get("topics") or []
    cache = _issuecard_load()
    eligible, archived_now = _eligible_subjects(topics)
    out = []
    for t in eligible[:FOCUS_TOPN]:
        sessions = t["sessions"][:FOCUS_ISSUES]
        issues = []
        for s in sessions:
            issues.append({
                "id": s["id"],
                "project": s.get("project", ""),
                "label": s.get("label", ""),
                "msgs": s.get("msgs"),
                "start": s.get("start"),   # when the session was created
                "end": s.get("end"),       # last prompt sent
                "recap": s.get("recap"),
                "ctxTokens": s.get("ctxTokens"),
                "ctxPct": s.get("ctxPct"),
                "card": (cache.get(s["id"]) or {}).get("card"),
            })
        recent = sessions[0] if sessions else {}
        out.append({
            "id": t["id"],
            "title": t["title"],
            "project": recent.get("project", ""),
            "source": t.get("source"),
            "sessionCount": t["sessionCount"],
            "issueCount": len(issues),
            "lastActivity": t["lastActivity"],
            "resumeSessionId": recent.get("id"),
            "status": _subject_status(issues),
            "issues": issues,
        })
    return {
        "generated_at": datetime.datetime.now(datetime.timezone.utc).isoformat(),
        "subjects": out,
        "totalSubjects": len(topics),
        "eligibleSubjects": len(eligible),
        "archived": archived_now,
    }


def _recap_worker():
    """Background daemon: backfill recaps + issue cards, refresh changed ones each loop."""
    while True:
        try:
            n = _recap_refresh()
            if n:
                print(f"[recaps] generated/updated {n} session recap(s)")
        except Exception as e:
            print(f"[recaps] worker error: {e}")
        try:
            m = _issuecard_refresh()
            if m:
                print(f"[issuecards] generated/updated {m} issue card(s)")
        except Exception as e:
            print(f"[issuecards] worker error: {e}")
        time.sleep(RECAP_INTERVAL)


def _build_context():
    now = time.time()
    if _ctx_cache["data"] and now - _ctx_cache["at"] < CACHE_TTL:
        return _ctx_cache["data"]

    # Scan every session log (capped high only as a runaway guard) so counts are
    # accurate. Reading a few hundred small logs is cheap and the result is cached.
    files = sorted(glob.glob(os.path.join(SESS_DIR, "*.jsonl")),
                   key=os.path.getmtime, reverse=True)[:500]
    sessions = []
    for f in files:
        s = _scan_session(f)
        pj = _proj_of(s["cwd"])
        if not pj or not s["ts0"] or s["n"] == 0:
            continue
        sessions.append(dict(
            id=os.path.basename(f)[:-6],  # filename stem = sessionId
            cwd=s["cwd"], edited=s["edited"],
            project=pj[0], label=pj[1], isRoot=pj[2],
            branch=s["branch"], start=s["ts0"], end=s["ts1"],
            durationMin=_dur_min(s["ts0"], s["ts1"]),
            prompt=(s["first"] or "")[:200],
            lastPrompt=(s["last"] or "")[:200],
            summary=s["summary"], msgs=s["n"],
            ctxTokens=s["ctxTokens"], ctxPeak=s["ctxPeak"],
            ctxPct=round(s["ctxTokens"] / CONTEXT_WINDOW * 100),
            ctxPeakPct=round(s["ctxPeak"] / CONTEXT_WINDOW * 100),
        ))
    sessions.sort(key=lambda x: x["end"], reverse=True)

    # attach cached AI recaps (the background worker keeps these fresh)
    rcache = _recap_load()
    for s in sessions:
        s["recap"] = (rcache.get(s["id"]) or {}).get("recap")

    # resolve topics (hybrid fallback chain) across ALL sessions
    _assign_topics(sessions)
    topics = {}
    for s in sessions:
        tp = topics.setdefault(s["_key"], dict(
            id=s["_key"], title=s["_title"], source=s["_src"],
            sessionCount=0, lastActivity=s["end"], sessions=[]))
        tp["sessionCount"] += 1
        if s["end"] > tp["lastActivity"]:
            tp["lastActivity"] = s["end"]
        tp["sessions"].append({k: s[k] for k in (
            "id", "project", "label", "branch", "start", "end", "prompt",
            "lastPrompt", "summary", "msgs", "durationMin", "recap",
            "ctxTokens", "ctxPct", "ctxPeak", "ctxPeakPct")})
    topic_list = sorted(topics.values(), key=lambda x: x["lastActivity"], reverse=True)

    # aggregate projects (skip the bare MyProjects root — those are meta chats)
    week_ago = (datetime.datetime.now(datetime.timezone.utc) -
                datetime.timedelta(days=7)).isoformat()
    projects = {}
    for s in sessions:
        if s["isRoot"]:
            continue
        p = projects.setdefault(s["project"], dict(
            name=s["project"], label=s["label"], lastActivity=s["end"],
            firstSeen=s["start"], sessionCount=0, minutes=0, path=s["cwd"],
            lastSessionId=s["id"],
            lastPrompt=s["lastPrompt"] or s["prompt"], bigPicture=s["prompt"]))
        p["sessionCount"] += 1
        p["minutes"] += s["durationMin"]
        if s["end"] > p["lastActivity"]:
            p["lastActivity"] = s["end"]
            p["path"] = s["cwd"]
            p["lastSessionId"] = s["id"]
            p["lastPrompt"] = s["prompt"]  # opening prompt of most-recent session
        if s["start"] < p["firstSeen"]:
            p["firstSeen"] = s["start"]
            p["bigPicture"] = s["prompt"]
    proj_list = sorted(projects.values(), key=lambda x: x["lastActivity"], reverse=True)
    # genuinely most-active project = most sessions (tie-break: most recent)
    most_active = max(
        proj_list,
        key=lambda p: (p["sessionCount"], p["lastActivity"]),
        default=None,
    )

    data = dict(
        generated_at=datetime.datetime.now(datetime.timezone.utc).isoformat(),
        resume=proj_list[0] if proj_list else None,
        projects=proj_list[:4],
        projectCount=len(proj_list),
        mostActive=most_active,
        topics=topic_list,
        sessions=sessions[:8],
        sessionCount=len(sessions),
        sessionsThisWeek=sum(1 for s in sessions if s["end"] >= week_ago),
    )
    _ctx_cache.update(at=now, data=data)
    return data


